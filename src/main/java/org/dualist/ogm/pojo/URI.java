package org.dualist.ogm.pojo;

public class URI {

	private String uri;

	public URI() {
	}
	
	public URI(String uri){
		this.uri = uri;
	}

	/**
	 * @return the uri
	 */
	public String getUri() {
		return uri;
	}

	/**
	 * @param uri the uri to set
	 */
	public void setUri(String uri) {
		this.uri = uri;
	}
}
